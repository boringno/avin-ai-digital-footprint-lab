#!/usr/bin/env node

/**
 * Scan Markdown files in the repository, read the first H1 from each file,
 * and regenerate docs-index.md in a deterministic way.
 */

const fs = require("fs/promises");
const path = require("path");

const REPO_ROOT = process.cwd();
const OUTPUT_FILE = path.join(REPO_ROOT, "docs-index.md");
const SKIP_DIRECTORIES = new Set([".git", "node_modules"]);
const SKIP_FILES = new Set(["docs-index.md"]);

async function walkMarkdownFiles(directory) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      if (!SKIP_DIRECTORIES.has(entry.name)) {
        files.push(...(await walkMarkdownFiles(fullPath)));
      }
      continue;
    }

    if (!entry.isFile()) {
      continue;
    }

    if (path.extname(entry.name).toLowerCase() !== ".md") {
      continue;
    }

    if (SKIP_FILES.has(entry.name)) {
      continue;
    }

    files.push(fullPath);
  }

  return files;
}

async function readFirstHeading(filePath) {
  const content = await fs.readFile(filePath, "utf8");
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1].trim() : path.basename(filePath, ".md");
}

function toRepoRelativePath(filePath) {
  return path.relative(REPO_ROOT, filePath).split(path.sep).join("/");
}

function groupLabel(relativePath) {
  const parts = relativePath.split("/");
  return parts.length === 1 ? "Root Documents" : parts[0];
}

async function main() {
  try {
    const markdownFiles = await walkMarkdownFiles(REPO_ROOT);
    const entries = [];

    for (const filePath of markdownFiles.sort()) {
      entries.push({
        path: toRepoRelativePath(filePath),
        title: await readFirstHeading(filePath),
      });
    }

    const groups = new Map();

    for (const entry of entries) {
      const label = groupLabel(entry.path);
      if (!groups.has(label)) {
        groups.set(label, []);
      }
      groups.get(label).push(entry);
    }

    const lines = [
      "# Docs Index",
      "",
      "This file is generated by `scripts/generate-docs-index.js`.",
      "",
      "It lists Markdown documents in this repository by folder and first H1 title.",
      "",
    ];

    for (const label of Array.from(groups.keys()).sort()) {
      lines.push(`## ${label}`);
      lines.push("");

      for (const entry of groups.get(label)) {
        lines.push(`- [${entry.title}](${entry.path})`);
      }

      lines.push("");
    }

    const nextContent = `${lines.join("\n").trim()}\n`;
    let currentContent = "";

    try {
      currentContent = await fs.readFile(OUTPUT_FILE, "utf8");
    } catch (error) {
      if (error.code !== "ENOENT") {
        throw error;
      }
    }

    if (currentContent === nextContent) {
      console.log("docs-index.md is already up to date.");
      return;
    }

    await fs.writeFile(OUTPUT_FILE, nextContent, "utf8");
    console.log("Updated docs-index.md");
  } catch (error) {
    console.error(error.message);
    process.exitCode = 1;
  }
}

main();
